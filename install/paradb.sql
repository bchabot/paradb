	geomag_kp		VARCHAR(35) NOT NULL,
	geomag_ap		INT NOT NULL,
	geomag_summary		VARCHAR(30) NOT NULL,
	geomag_list		VARCHAR(100) NOT NULL,
	geomag_plot		VARCHAR(100) NOT NULL,
	xray_long		FLOAT NOT NULL,
	xray_short		FLOAT NOT NULL,
	xray_high		FLOAT NOT NULL,
	xray_low		FLOAT NOT NULL,
	xray_peak		VARCHAR(30) NOT NULL,
	xray_summary		VARCHAR(30) NOT NULL,
